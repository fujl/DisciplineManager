#import "DMDetailVoteRequester.h"
#import "DMVoteDetailInfo.h"

@interface DMDetailVoteRequester () <DMRequesterDelegate>

@end

@implementation DMDetailVoteRequester

- (instancetype)init {
    self = [super init];
    if (self) {
        self.delegate = self;
    }
    return self;
}

#pragma mark - HlwyyRequesterDelegate
- (id)onDumpData:(NSDictionary *)jsonObject {
    NSDictionary *errData = [jsonObject objectForKey:@"errData"];
    DMVoteDetailInfo *vdInfo = [[DMVoteDetailInfo alloc] initWithDict:errData];
    return vdInfo;
}

- (id)onDumpErrorData:(NSDictionary *)jsonObject {
    return [jsonObject objectForKey:@"errMsg"];
}

- (NSString *)getChildrenUrl {
    return @"jgxt/api/vote/get.do";
}

/**
 *  放入请求的基本参数
 *
 *  @param params 参数容器，注意：里面已经放入基本的请求参数
 */
- (void)onPutParams:(NSMutableDictionary *)params {
    NSString *userId = [params objectForKey:@"userId"];
    [params removeAllObjects];
    params[@"userId"] = userId;
    params[@"id"] = self.vlId;
}

- (BOOL)onPostJson {
    return YES;
}

@end
